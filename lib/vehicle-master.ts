/** 풀네임 자동 결합 (저장용). 빈 값은 자동 스킵. */
export function buildVehicleFullName(parts: {
  maker?: string;
  model?: string;
  subModel?: string;
  variant?: string;
  trim?: string;
}): string {
  return [parts.maker, parts.model, parts.subModel, parts.variant, parts.trim]
    .map((s) => (s ?? '').trim())
    .filter(Boolean)
    .join(' ');
}
